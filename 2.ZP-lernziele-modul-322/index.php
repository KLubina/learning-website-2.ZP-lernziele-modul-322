        <h2>1. Prinzipien der Dialoggestaltung gemäß ISO-9241-110:</h2>
        <ul>
            <li>Aufgabenangemessenheit: Das System unterstützt den Benutzer effektiv bei der Erledigung seiner Aufgabe.</li>
            <li>Selbstbeschreibungsfähigkeit: Jeder Schritt im Dialog erklärt sich durch Rückmeldung des Systems selbst.</li>
            <li>Steuerbarkeit: Der Benutzer kann den Dialogablauf steuern und seine Richtung und Geschwindigkeit beeinflussen.</li>
            <li>Erwartungskonformität: Das System verhält sich konsistent und entspricht den bekannten Konventionen.</li>
            <li>Fehlertoleranz: Das System ermöglicht ein effektives und effizientes Beheben von Fehlern.</li>
            <li>Individualisierbarkeit: Das System lässt sich an die Bedürfnisse des individuellen Benutzers anpassen.</li>
            <li>Lernförderlichkeit: Das System unterstützt den Benutzer beim Erlernen der Nutzung.</li>
        </ul>

        <h2>2. Korrekte Design-Prinzipien:</h2>
        <ul>
            <li>Navigation: Sollte intuitiv, einfach und konsistent gestaltet sein, um eine gute Benutzerführung zu gewährleisten.</li>
            <li>Bedienelemente: Müssen klar erkennbar und verständlich sein, damit Benutzer effektiv mit dem System interagieren können.</li>
            <li>Startseite: Sollte einen klaren Einstiegspunkt bieten und Zugriff auf wesentliche Funktionen ermöglichen.</li>
            <li>Pflichtfelder: Sollten deutlich markiert sein, um dem Benutzer die notwendigen Aktionen zur Aufgabenerfüllung zu verdeutlichen.</li>
        </ul>

        <h2>3. Methoden zur Hilfestellung für Benutzer:</h2>
        <ul>
            <li>Online-Hilfen: Tooltips, modale Dialoge, integrierte Hilfeabschnitte.</li>
            <li>FAQs und Support-Seiten: Um häufig auftretende Fragen zu klären.</li>
            <li>Chatbots und Live-Support: Für Echtzeit-Hilfestellungen und interaktive Unterstützung.</li>
            <li>Benutzerhandbücher und Tutorials: Anleitungen, die Benutzern helfen, Funktionen des Systems zu verstehen und zu nutzen.</li>
        </ul>

        <h2>4. Visualisierung (SideQuest 322-04A - 01.00):</h2>
        <ul>
            <li>Zweck der Visualisierung: Unterstützt das Verstehen komplexer Informationen und Daten durch graphische Darstellungen. Erleichtert schnelles Erfassen und Interpretieren von Inhalten.</li>
        </ul>

        <h2>5. Iteratives Arbeiten:</h2>
        <ul>
            <li>Definition: Ein Prozessmodell, bei dem ein Projekt in mehreren Iterationen bearbeitet wird, wobei jede Iteration Planung, Ausführung und Bewertung umfasst. Es ermöglicht kontinuierliche Verbesserungen basierend auf Feedback und Anpassung an sich ändernde Anforderungen.</li>
        </ul>

        <p>Diese Punkte bilden die Grundlage für effektives Lernen und Umsetzen der Prinzipien im Bereich der Dialoggestaltung und darüber hinaus.</p>
